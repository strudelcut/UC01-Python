# Construa uma página/programa onde o usuário digitará dois números e o programa exibirá, na tela, o resultado das quatros operações básicas da matemática.

num = int(input("Digite um numero "))
num1 = int(input("Digite mais um número "))

print(f"A soma deu {num + num1}")
print(f"A subtração deu {num - num1}")
print(f"A multiplicação deu {num * num1}")
print(f"A divisão deu {num / num1}")